# Counter for Numbers till 100:
for x in range (1, 101):
    print(x, end=", ")
print("\n")

# Counter for Reversed Numbers:
for x in reversed(range (1, 101)):
    print(x, end=", ")
print("\n")

# Counter for Odd Numbers: 
for x in range (1, 101, 2):
    print(x, end=", ")
print("\n")

# Counter for Even Numbers:
for x in range (2, 101, 2):
    print(x, end=", ")
print("\n")