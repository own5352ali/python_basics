
Num_1 = float(input("Enter the 1st Number: "))
Num_2 = float(input("Enter the 2nd Number: "))

Operator = input("Select the operator (+, -, *, /)")

if Operator == "+":
    Result = Num_1 + Num_2
    print(Result)

elif Operator == "-":
    Result = Num_1 - Num_2
    print(Result)

elif Operator == "*":
    Result = Num_1 * Num_2
    print(Result)

elif Operator == "/":
    Result = Num_1 / Num_2
    print(Result)
